											<p class="text-center">Janusz Zalewski is a professor of Computer Science and Software Engineering at Florida Gulf Coast University, in Ft. Myers, Florida, USA. He obtained his Ph.D. in Computer Science and Engineering in 1979 from the Faculty of Electrical Engineering at Warsaw University of Technology. He previously held academic positions at Embry-Riddle Aeronautical University and University of Central Florida and joined Florida Gulf Coast University in 2002.</p>

											<p class="text-center">In the past, he worked on projects for the Superconducting Super Collider, Lawrence Livermore National Laboratory, NASA, FAA, Air Force Research Labs, and Nuclear Regulatory Commission, as well as for a number of private companies, including Lockheed Martin, Harris, Boeing, and others. He also had fellowships at NASA and Air Force Research Labs.</p>

											<p class="text-center">His research interests include real-time embedded and cyberphysical systems, safety and security of complex computer systems and networks, and software engineering education. He is a member of the IFAC TC 3.1 on Computers for Control, and the Secretary of the IEEE P1876 Working Group “Networked Smart Learning Objects for Online Laboratories”. He was recently featured on the College Website.</p>

